    <h2>永續經營政策</h2>
    <p>為求本公司可永續發展，在本公司投資者及其他利害關係人良好的溝通基礎下，給與以下承諾：</p>
    <ul style="margin: 10px 0 10px 15px;">
        <li>營運過程應於環境保護議題上，透過定期環境危害源監控和環境管制措施以落實節能減碳、環境污染源減量。</li>
        <li>應善盡社會責任，以健全合法的福利制度照顧公司員工，同時應回饋社會，以各式公益活動方式提供給弱勢團體適當資源協助。</li>
        <li>落實公司經營理念，專業、誠信、共享、創新，力求公司治理方向可更加符合ESG精神。</li>
    </ul>
    <p>除了經濟上的盈利外，更關注社會的發展與共好，專注於創造共享價值，達到社會、企業、顧客、員工的和諧發展，從物質層次到精神層次，來改善社會環境與人們的生活。</p>
