<h3>檢舉信箱作業辦法</h3>
<p>
	本公司為落實誠信經營政策，並積極防範不誠信行為，設立檢舉信箱機制,嚴格禁止任何違反從業道德之行為及情事，如有違反各營運所在相關法令或本公司各項從業道德行為準則，本公司一律秉公處理，絕不寬貸。
    <br>
	本公司提供檢舉之獎勵：糾正或檢舉違反廉潔守則情事，凡經查證屬實者，將予適當之獎勵。另外針對違反從業道德行為，除所獲取之各項不正當利益，均應追繳發還被索取人或公司外，並依情節之大小，予以處分或合併處分。
</p>

