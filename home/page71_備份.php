<h3 style="color: #002CFF;">檢舉信箱作業辦法</h3>
<ul style="list-style-type:decimal; margin: 0 0 0 15px; line-height: 35px;">
	<li>本公司為落實誠信經營政策，並積極防範不誠信行為，設立檢舉信箱機制,嚴格禁止任何違反從業道德之行為及情事，如有違反各營運所在相關法令或本公司各項從業道德行為準則，本公司一律秉公處理，絕不寬貸。</li>
	<li>本公司提供檢舉之獎勵：糾正或檢舉違反廉潔守則情事，凡經查證屬實者，將予適當之獎勵。另外針對違反從業道德行為，除所獲取之各項不正當利益，均應追繳發還被索取人或公司外，並依情節之大小，予以處分或合併處分。</li>
	<li>本公司檢舉方式如下:
	    <p style="color: #f00;">■ 檢舉信箱 : <a href="mailto:whistleblower@lasertek.com.tw">whistleblower@lasertek.com.tw</a></p>
	    <p style="color: #f00;">■ LINE檢舉帳號 : lt-whistleblower</p>
	    <p style="color: #f00;">■ 微信檢舉帳號 : lt-whistleblower</p>
	</li>
	<li>檢舉人應至少提供下列資訊始得成立檢舉案件：
	    <p style="color: #f00;">■ 內部員工 : (1) 廠別 (2) 工號 (3) 姓名。</p>
	    <p style="color: #f00;">■ 外部人 : (1) 姓名 (2) 身分證號。</p>
	    <p style="color: #f00;">■ 可聯絡到檢舉人之地址、電話、電子信箱。</p>
	    <p style="color: #f00;">■ 被檢舉人之姓名或其他足資識別被檢舉人身分特徵之資料。</p>
	    
	</li>
	<li>本公司接收檢舉情事唯一窗口為稽核主管陳先生，電話(07)8159877 #509，對於檢舉人身分及檢舉內容將予以保密，本公司並承諾保護檢舉人不因檢舉情事而遭不當處置。</li>
	<li>本公司對於檢舉案件一經成立即進行相關調查,調查進度及結果僅得由檢舉人知悉。</li>
	<li>本辦法呈總經理核准後公佈實施，修訂時亦同。</li>
</ul>
